/*
Измените переменные и строку вывода в программе так,
чтобы она расчитывала и печатала следующее выражение
3*3+4*4=25
 */

class TaskA3 {
    public static void main(String[] args) {
        int i = 4;
        int j = 3;
        int k = j*j+i*i;
        System.out.println(i + "*" + i + "+" + j + "*" + j + "=" + k);
    }
}
